// Develop a program to do addition, subtraction, multiplication, and division.

// Declare header files
#include <iostream>
using namespace std;

// Main program
int main() {
	// Declare variables
	double num1 = 7;
	double num2 = 2;

	// Print addition
	cout << num1 << " + " << num2 << " = " << (num1 + num2) << endl;

	// Print subtraction
	cout << endl << num1 << " - " << num2 << " = " << (num1 - num2) << endl;

	// Print multiplication
	cout << endl << num1 << " * " << num2 << " = " << (num1 * num2) << endl;

	// Print divison
	cout << endl << num1 << " / " << num2 << " = " << (num1 / num2) << endl;

	return 0;
}